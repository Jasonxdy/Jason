package testProject;
// 패키지 선언 : 현재 class 파일이 어디에 위치해 있는데 표시 (선언)

// class 선언부 : 클래스의 이름을 선언하는 부분
public class HelloWorld {
	// 클래스 작성부 : 주석, 패키지 선언을 제외한 모든 코드를 작성함
	
	// { } (블럭) : 관련된 영역을 나눠주는 부분
	
	// method 선언부 : 실행할 기능을 선언
	// ** main method - java 애플리케이션을 수해하기 위해 필수로 존재해야 하는  method
	public static void main(String[] args) {
		
		// method 작성부 : 실행될 코드를 작성
		System.out.println("Hello World!!");
		// 콘솔 (System.out)에 "Hello World!!"라는 문자열("문자열")을 출력 (println)해라
		
	}
}
